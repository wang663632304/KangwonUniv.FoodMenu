package kr.yudonguk.util.calendar;

import java.security.InvalidParameterException;

/**
 * LunaDate는 Immutable 객체이다.
 */
public class LunaDate
{
	public final int year;
	public final int month;
	public final int day;
	public final boolean isLeapMonth;

	public LunaDate(int year_, int month_, int day_)
	{
		this(year_, month_, day_, false);
	}

	public LunaDate(int year_, int month_, int day_, boolean isLeapMonth_)
	{
		if (!isValid(year_, month_, day_, isLeapMonth_))
		{
			StringBuilder stringBuilder = new StringBuilder();
			stringBuilder.append("Invalid Paramter >> ");
			stringBuilder.append("Year : " + year_);
			stringBuilder.append(", Month : " + month_);
			stringBuilder.append(", Day : " + day_);
			stringBuilder.append(", Leap Month : " + isLeapMonth_);
			throw new InvalidParameterException(stringBuilder.toString());
		}

		year = year_;
		month = month_;
		day = day_;
		isLeapMonth = isLeapMonth_;
	}

	@Override
	public boolean equals(Object obj)
	{
		if (obj instanceof LunaDate)
		{
			LunaDate date = (LunaDate) obj;

			if (date.year == year && date.month == month && date.day == day
					&& date.isLeapMonth == isLeapMonth)
				return true;
		}
		return super.equals(obj);
	}

	@Override
	public String toString()
	{
		StringBuilder stringBuilder = new StringBuilder(super.toString());
		stringBuilder.append("[");
		stringBuilder.append(getClass().getName());
		stringBuilder.append(">>");
		stringBuilder.append("year : " + year);
		stringBuilder.append(", month : " + month);
		stringBuilder.append(", day : " + day);
		stringBuilder.append(", isLeapMonth : " + isLeapMonth);
		stringBuilder.append("]");

		return stringBuilder.toString();
	}

	public boolean isLeapYear()
	{
		return LunaData.leapMonth[year - LunaData.startYear] != 0;
	}
	
	public int getLeapMonth()
	{
		return LunaData.leapMonth[year - LunaData.startYear];
	}

	public ModifiedJulianDay toModifiedJulianDay()
	{
		long totalDay = 0;

		int yearIndex = 0;
		final int maxYearIndex = year - LunaData.startYear;

		if (maxYearIndex >= 50)
		{
			// 이전 반세기 까지의 일수 합을 구해야하므로 -1을 합한다.
			int halfACenturyIndex = (maxYearIndex / 50) - 1;
			totalDay = LunaData.cumulativeDaysOfHalfACentury[halfACenturyIndex];
			yearIndex = (halfACenturyIndex + 1) * 50;
		}

		// 이전 연도 까지의 일수를 합해야 하므로,
		// maxYearIndex의 연도는 합하지 않는다.
		for (; yearIndex < maxYearIndex; yearIndex++)
		{
			totalDay += LunaData.daysOfYear[yearIndex];
		}

		// 이전 월 까지의 일수를 합해야 하므로,
		// maxMonthIndex의 월은 합하지 않는다.
		final int leapMonthIndex = LunaData.leapMonth[maxYearIndex] - 1;
		for (int monthIndex = 0, maxMonthIndex = month - 1; monthIndex < maxMonthIndex; monthIndex++)
		{
			if (monthIndex == leapMonthIndex)
			{
				totalDay += LunaData.daysOfMonth[maxYearIndex][monthIndex];
				totalDay += LunaData.daysOfMonth[maxYearIndex][12];
			}
			else
			{
				totalDay += LunaData.daysOfMonth[maxYearIndex][monthIndex];
			}
		}

		// 현재 달이 윤달일 경우 위의 코드에 의해
		// 윤달 전달의 일수가 합해지지 않았으므로, 따로 합해준다.
		// 예를 들어 현재달이 윤3월일 경우, 3월은 합산되지 않았으므로,
		// 3월의 일수를 합산해준다.
		if (isLeapMonth)
			totalDay += LunaData.daysOfMonth[maxYearIndex][leapMonthIndex];
		totalDay += day;

		long modifiedJulianDay = (totalDay - 1) + LunaData.startDay.getDay();

		return new ModifiedJulianDay(modifiedJulianDay);
	}

	public static LunaDate convert(ModifiedJulianDay modifiedJulianDay)
	{
		if (LunaData.startDay.getDay() > modifiedJulianDay.getDay()
				|| modifiedJulianDay.getDay() > LunaData.endDay.getDay())
			return null;

		long totalDay = modifiedJulianDay.getDay() - LunaData.startDay.getDay()
				+ 1;

		int yearIndex = 0;
		if (totalDay > LunaData.cumulativeDaysOfHalfACentury[0])
		{
			yearIndex += 50;

			for (int i = 1; i < LunaData.cumulativeDaysOfHalfACentury.length; i++)
			{
				if (totalDay <= LunaData.cumulativeDaysOfHalfACentury[i])
				{
					totalDay -= LunaData.cumulativeDaysOfHalfACentury[i - 1];
					break;
				}

				yearIndex += 50;
			}
		}

		for (; yearIndex < LunaData.daysOfYear.length; yearIndex++)
		{
			long diff = totalDay - LunaData.daysOfYear[yearIndex];
			if (diff <= 0)
				break;

			totalDay = diff;
		}
		yearIndex = Math.min(yearIndex, LunaData.daysOfYear.length - 1);

		final int leapMonthIndex = LunaData.leapMonth[yearIndex] - 1;
		boolean isLeapMonth = false;
		int monthIndex = 0;
		for (; monthIndex < 12; monthIndex++)
		{
			long diff = totalDay - LunaData.daysOfMonth[yearIndex][monthIndex];
			if (diff <= 0)
				break;

			totalDay = diff;

			// monthIndex로는 윤달을 판별 못하므로,
			// monthIndex가 윤달- 1 과 같을 때 윤달 검사 및
			// 그에 대한 감산을 한다.
			if (monthIndex == leapMonthIndex)
			{
				diff = totalDay - LunaData.daysOfMonth[yearIndex][12];
				if (diff <= 0)
				{
					isLeapMonth = true;
					break;
				}

				totalDay = diff;
			}
		}
		monthIndex = Math.min(monthIndex, 12 - 1);

		return new LunaDate(LunaData.startYear + yearIndex, 1 + monthIndex,
				(int) totalDay, isLeapMonth);
	}

	private static boolean isValid(int year_, int month_, int day_,
			boolean isLeapMonth_)
	{
		if (year_ < LunaData.startYear || year_ > LunaData.endYear)
			return false;

		if (month_ < 1 || month_ > 12)
			return false;

		int yearIndex = year_ - LunaData.startYear;

		if (LunaData.leapMonth[yearIndex] != month_ && isLeapMonth_)
			return false;

		if (isLeapMonth_)
		{
			int monthIndex = LunaData.daysOfMonth[yearIndex].length - 1;
			if (day_ < 1 || day_ > LunaData.daysOfMonth[yearIndex][monthIndex])
				return false;
		}
		else
		{
			int monthIndex = month_ - 1;
			if (day_ < 1 || day_ > LunaData.daysOfMonth[yearIndex][monthIndex])
				return false;
		}

		return true;
	}
}

